package Chapter5;

//Abstract Classes

//A class that contains at least one abstract method is called an abstract class and must
//be declared with the abstract modifier on the class declaration.

//To create an abstract method, you specify the modifier abstract and replace the
//method body with a semicolon.

public abstract class Ball
{
public abstract int hit(int batSpeed);
}

//Here the method named hit is declared as an abstract method that returns an int
//value and accepts an int parameter.
