//Using the Enhanced for Loop

// An enhanced for loop is designed to simplify loops that process arrays and collections.

//When it’s used with an array, the enhanced for loop has this format:

//for (type identifier : array)
//{
//Statements ...
//}

//The type identifies the type of the elements in the array, and the identifier provides
//a name for a local variable that is used to access each element. array names
//the array you want to process.

//Here’s an example:

//String[] days = { "Sunday", "Monday", "Tuesday",
//"Wednesday", "Thursday",
//"Friday", "Saturday" };
//for (String day : days)
//{
//System.out.println(day);
//}

//This loop prints the following lines to the console:

//Sunday
//Monday
//Tuesday
//Wednesday
//Thursday
//Friday
//Saturday

//In other words, it prints each of the strings in the array on a separate line.







